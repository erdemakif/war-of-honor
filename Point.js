/*
	Information;
		- Point class allows you to create a point object which holds information of a
		pair of numbers, simply X and Y

	variables;
		- px: [Number] Represents X coordinate of the point
		- py: [Number] Represents Y coordinate of the point
	
	methods;

*/

function Point(px, py){
	this.px = px;
	this.py = py;
}